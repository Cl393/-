namespace MathLib
{
    public class Calculator
    {
        public static double Add(double x, double y) => x + y;
        /// <summary>
        /// 舰队副司令就是浪费
        /// </summary>
        /// <param name="x"></param>
        /// <param name="y"></param>
        /// <returns></returns>
        public static double Subtract(double x, double y) => x - y;
    }
}
